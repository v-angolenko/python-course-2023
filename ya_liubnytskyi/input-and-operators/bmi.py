# Напишіть програму для розрахунку індексу маси тіла (body mass index - BMI) людини. 
# Користувач має ввести свій зріст і вагу, 
# після чого ви використовуєте одну з наведених нижче формул для визначення індексу: 
# BMI = вага (кг) / зріст^2 (м)

weight = float(input("Введіть свою вагу (кг) "))
height = float(input("Введіть свій зріст (м) "))

bmi = weight/(height*height)
print("Ваш індекс маси тіла", round(bmi, 1))
