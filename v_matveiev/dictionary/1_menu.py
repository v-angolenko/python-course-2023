# Тема: Обробка замовлення в кафе з використанням циклів та словників

# Завдання:
# Уявіть, що ви пишете програму для кафе, де клієнти можуть замовляти різні напої.
# Створіть словник `menu_prices`, де ключами будуть назви напоїв, а значеннями - їх ціни.
# Після цього, напишіть код, який запитує у користувача, що він хоче замовити,
# і додає ціну обраного напою до загальної суми замовлення. Якщо користувач вводить "end",
# програма повинна вивести загальну суму замовлення.

# Очікуваний результат:
# Наприклад, якщо в `menu_prices` є напої {"coffee": 10, "tea": 7, "water": 5},
# і користувач замовляє "coffee" та "water", а потім вводить "end",
# програма виводить "Загальна сума замовлення: 15".

# Послідовність виконання:
# 1. Створіть словник `menu_prices` з назвами страв та їхніми цінами.
# 2. Ініціалізуйте змінну для зберігання загальної вартості замовлення.
# 3. Використовуйте цикл для читання вводу від користувача.
# 4. Якщо введено назву страви, яка є в словнику, додайте її вартість до загальної суми.
# 5. Якщо введено "end", виведіть загальну вартість замовлення.

def create_menu():
    return {"coffee": 10, "tea": 7, "water": 5}
def take_order(menu_prices):
    total_order_price = 0
    while True:
        order_item = input("Введіть назву напою (або 'end' для завершення замовлення): ").lower()
        if order_item in menu_prices:
            total_order_price += menu_prices[order_item]
            print(f"Додано {order_item} за {menu_prices[order_item]}. Загальна сума: {total_order_price}")
        elif order_item == "end":
            print(f"Загальна сума замовлення: {total_order_price}")
            break
        else:
            print("Неправильний вибір. Будь ласка, виберіть напій зі списку.")
if __name__ == "__main__":
    menu_prices = create_menu()
    take_order(menu_prices)




