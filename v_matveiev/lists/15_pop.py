# Тема: Видалення елемента зі списку за допомогою методу list.pop() із застосуванням input.

# Завдання: Уявіть, що ви розробляєте простий менеджер завдань (todo list).
# Створіть список із назвою `tasks`, який спочатку містить такі завдання:
# "Зробити домашнє завдання", "Піти в магазин", "Подзвонити другу", "Приготувати обід", "Прочитати книгу".
# Програма повинна вивести список завдань, після чого запитати у користувача "Яке завдання ви виконали?
# Введіть номер:", де номер відповідає індексу завдання в списку, починаючи з 1.
# Видаліть відповідне завдання зі списку за допомогою методу `pop()` і виведіть оновлений список на екран.

# Очікуваний результат: Перш ніж користувач введе номер завдання, програма виводить список завдань:

# 1. Зробити домашнє завдання
# 2. Піти в магазин
# 3. Подзвонити другу
# 4. Приготувати обід
# 5. Прочитати книгу

# Якщо користувач вводить номер 3, оновлений список має виглядати так:

# 1. Зробити домашнє завдання
# 2. Піти в магазин
# 3. Приготувати обід
# 4. Прочитати книгу
# ```
tasks = ["Зробити домашнє завдання", "Піти в магазин", "Подзвонити другу", "Приготувати обід", "Прочитати книгу"]
print("Список завдань:")
for i, task in enumerate(tasks, start=1):
    print(f"{i}. {task}")
task_number = int(input("Яке завдання ви виконали? Введіть номер: "))
if 1 <= task_number <= len(tasks):
    removed_task = tasks.pop(task_number - 1)
    print(f"Ви виконали завдання: {removed_task}")
    print("Оновлений список завдань:")
    for i, task in enumerate(tasks, start=1):
        print(f"{i}. {task}")
else:
    print("Введений номер завдання некоректний.")
