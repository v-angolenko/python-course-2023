# Тема: Розрахунок середньої вартості обідів за тиждень.

# Завдання:
# Напишіть програму, яка запитує в користувача вартість обіду в ресторані за кожен день
# тижня (понеділок через неділю), зберігає введені дані в змінні, після чого розраховує
# та виводить на екран середню вартість обіду за тиждень.

# Очікуваний результат:
# ```
# Введіть вартість обіду в понеділок: 90
# Введіть вартість обіду у вівторок: 85
# Введіть вартість обіду в середу: 80
# Введіть вартість обіду в четвер: 75
# Введіть вартість обіду в п'ятницю: 90
# Введіть вартість обіду в суботу: 100
# Введіть вартість обіду в неділю: 120

# Середня вартість обіду за тиждень: 91.43 грн.
# ```

# Створюємо пустий список для зберігання вартості обідів за тиждень
lunch_costs = []

# Запитуємо у користувача вартість обіду за кожен день тижня
lunch_costs.append(float(input("Введіть вартість обіду в понеділок: ")))
lunch_costs.append(float(input("Введіть вартість обіду в вівторок: ")))
lunch_costs.append(float(input("Введіть вартість обіду в середу: ")))
lunch_costs.append(float(input("Введіть вартість обіду в четвер: ")))
lunch_costs.append(float(input("Введіть вартість обіду в п'ятницю: ")))
lunch_costs.append(float(input("Введіть вартість обіду в суботу: ")))
lunch_costs.append(float(input("Введіть вартість обіду в неділю: ")))

# Обчислюємо середню вартість обідів за тиждень
average_cost = sum(lunch_costs) / len(lunch_costs)

# Виводимо результат
print(f"\nСередня вартість обіду за тиждень: {average_cost:.2f} грн.")
